
public class RequirementsRowData {
	
	String ID;
	String Type;
	String Class;
	String Category;
	String Descripton;
	String DateCreated;
	String Author;
	String Priority;
	
	RequirementsRowData(String id, String type, String clss, String cat,
						String desc, String dc, String auth, String pri)
	{
	ID = id;
	Type = type;
	Class = clss;
	Category = cat;
	Descripton = desc;
	DateCreated = dc;
	Author = auth;
	Priority = pri;
	}

}
